function showSidebar() {
    const sidebar = document.querySelector('aside ul')
    const sidebar2 = document.querySelector('aside i')
    const sidebar3 = document.querySelector('aside')
    const sidebar4 = document.querySelector('aside ul li')


    sidebar.classList.toggle('active')   
    sidebar2.classList.toggle('active')   
    sidebar3.classList.toggle('active')   
    sidebar4.classList.toggle('active')   
}